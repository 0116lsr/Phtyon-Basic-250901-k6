# ========================================
# 📌 print() 함수 학습
# ========================================
# 
# print()는 파이썬에서 가장 기본적인 함수로, 
# 괄호 안의 내용을 화면(콘솔)에 출력하는 역할을 합니다.
# 
# 🔍 다른 프로그래밍 언어와의 비교:
# - JavaScript: console.log()
# - Java: System.out.println()
# - C: printf()
# 
# 📖 더 자세한 내용: 학습가이드/01-파이썬기본문법.md#1-print-함수

print("헬로우 파이썬")  # 문자열 출력 - 따옴표 안의 텍스트가 그대로 출력됩니다
print("헬로우 파이썬")  # 같은 내용을 여러 번 출력 - 반복문을 배우면 더 효율적으로 할 수 있습니다
print("헬로우 파이썬")  # 반복문을 배우면 이렇게 반복하지 않아도 됩니다
print("헬로우 파이썬")  # 하지만 지금은 기본 문법을 익히는 단계입니다
print("헬로우 파이썬")  # 각 줄이 순서대로 실행됩니다 (위에서 아래로)

# 💡 학습 팁:
# - 파이썬은 들여쓰기(공백)가 매우 중요합니다
# - 주석은 # 기호로 시작하며, 코드 실행에 영향을 주지 않습니다
# - print() 함수는 여러 개의 값을 동시에 출력할 수도 있습니다
#   예: print("안녕", "하세요", 123)  # 결과: 안녕 하세요 123
